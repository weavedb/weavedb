const { isNil, is, intersection } = require("ramda")
const { err, wrapResult, parse } = require("../..//lib/utils")
const { validate } = require("../../lib/validate")

const setAlgorithms = async (
  state,
  action,
  signer,
  contractErr = true,
  SmartWeave,
  kvs,
  executeCron,
  depth = 1,
  type = "direct",
) => {
  if ((state.bundlers ?? []).length !== 0 && type === "direct") {
    err("only bundle queries are allowed")
  }

  let original_signer = null
  if (isNil(signer)) {
    ;({ signer, original_signer } = await validate(
      state,
      action,
      "setAlgorithms",
      SmartWeave,
      true,
      kvs,
    ))
  }

  let { _data, data, query, new_data, path } = await parse(
    state,
    action,
    "setAlgorithms",
    signer,
    null,
    contractErr,
    SmartWeave,
    kvs,
  )
  if (
    !is(Array)(new_data) ||
    intersection(new_data)(["secp256k1", "ed25519", "rsa256", "secp256k1-2"])
      .length !== new_data.length
  ) {
    err(`The wrong algorithms`)
  }
  state.auth.algorithms = new_data
  return wrapResult(state, original_signer, SmartWeave)
}

module.exports = { setAlgorithms }
